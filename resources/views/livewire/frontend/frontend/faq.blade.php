        <script>
            // Accordion functionality
            const buttons = document.querySelectorAll('button[class*="w-full px-8 py-6"]');
            buttons.forEach(button => {
                button.addEventListener('click', () => {
                    const parent = button.parentElement;
                    const content = parent.querySelector('div:not([class*="flex"])');
                    const icon = button.querySelector('svg');

                    // Close other accordions
                    buttons.forEach(otherButton => {
                        if (otherButton !== button) {
                            const otherParent = otherButton.parentElement;
                            const otherContent = otherParent.querySelector('div:not([class*="flex"])');
                            if (otherContent) {
                                otherContent.style.display = 'none';
                                otherButton.querySelector('svg').style.transform = 'rotate(0deg)';
                            }
                        }
                    });

                    // Toggle current accordion
                    if (content) {
                        const isOpen = content.style.display !== 'none';
                        content.style.display = isOpen ? 'none' : 'block';
                        icon.style.transform = isOpen ? 'rotate(0deg)' : 'rotate(180deg)';
                        icon.style.transition = 'transform 0.3s ease';
                    }
                });
            });
        </script>
